package com.viettel.bccs.inventory.model;

import javax.persistence.*;
import java.io.Serializable;

/**
 *
 * @author anhvv4
 */
@Entity
@Table(name = "DOMAIN")
@NamedQueries({
        @NamedQuery(name = "Domain.findAll", query = "SELECT b FROM Domain b")})
public class Domain implements Serializable {
public static enum COLUMNS {DESCRIPTION, DOMAIN, ID, STATUS, EXCLUSE_ID_LIST};
    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @Column(name = "ID")
    private Long id;
    @Column(name = "DOMAIN")
    private String domain;
    @Column(name = "DESCRIPTION")
    private String description;
    @Column(name = "STATUS")
    private String status;

    public Domain() {
    }

    public Domain(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Domain)) {
            return false;
        }
        Domain other = (Domain) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "base.com.viettel.bccs.inventory.model.Domain[ id=" + id + " ]";
    }

}
